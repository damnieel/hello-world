package com.moon.vip.infra.vo.sys.roleManage;

import java.io.Serializable;
import java.util.List;
/**
 * 
 * @ClassName: RoleDataOperVO 
 * @Description: 角色 数据 操作类型 数据类 
 * @author 大枫
 * @date 2016年1月14日 下午3:27:08 
 *
 */
public class RoleDataOperVO implements Serializable{

	private static final long serialVersionUID = -3271896208874124365L;
	/**
	 * 角色id
	 */
	private Integer roleId;
	/**
	 * 员工id
	 */
	private Integer id;
	/**
	 * 操作类型
	 */
	private String operType;
	/**
	 * 规则内容
	 */
	private List<String> roleContentList;
	/**
	 * 规则id
	 */
	private List<Integer> ruleIdList;
	
	@Override
	public String toString() {
		return "RoleDataOperVO [roleId=" + roleId + ", id=" + id + ", operType=" + operType + ", roleContentList="
				+ roleContentList + ", ruleIdList=" + ruleIdList + "]";
	}

	public Integer getRoleId() {
		return roleId;
	}

	public void setRoleId(Integer roleId) {
		this.roleId = roleId;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getOperType() {
		return operType;
	}

	public void setOperType(String operType) {
		this.operType = operType;
	}

	public List<String> getRoleContentList() {
		return roleContentList;
	}

	public void setRoleContentList(List<String> roleContentList) {
		this.roleContentList = roleContentList;
	}

	public List<Integer> getRuleIdList() {
		return ruleIdList;
	}

	public void setRuleIdList(List<Integer> ruleIdList) {
		this.ruleIdList = ruleIdList;
	}
}
