package com.moon.vip.infra.vo.sys;

import java.io.Serializable;

/**
 * 专业编号序列号VO
 * SequenceVO<BR>
 * 创建人:娴贵 <BR>
 * 时间：2016年1月19日-下午7:52:16 <BR>
 * @version 1.0.0
 * 
 */
public class SequenceVO implements Serializable{

	private static final long serialVersionUID = 4337083418804667834L;
	
	/***专业ID***/
	private Integer professionId;
	
	/***报名日期***/
	private String produceDate;
	
	/***序号***/
	private Integer sequence;
	
	/***专业前缀***/
	private String codeValue;
	
	/***版本***/
	private Integer version;
	

	public Integer getProfessionId() {
		return professionId;
	}

	public void setProfessionId(Integer professionId) {
		this.professionId = professionId;
	}

	public String getProduceDate() {
		return produceDate;
	}

	public void setProduceDate(String produceDate) {
		this.produceDate = produceDate;
	}

	public Integer getSequence() {
		return sequence;
	}

	public void setSequence(Integer sequence) {
		this.sequence = sequence;
	}

	public Integer getVersion() {
		return version;
	}

	public void setVersion(Integer version) {
		this.version = version;
	}

	@Override
	public String toString() {
		return "SequenceVO [professionId=" + professionId + ", produceDate=" + produceDate + ", sequence=" + sequence
				+ ", codeValue=" + codeValue + ", version=" + version + "]";
	}

	public String getCodeValue() {
		return codeValue;
	}

	public void setCodeValue(String codeValue) {
		this.codeValue = codeValue;
	}
	
}
